def iter_group_matches() -> list[tuple[str, int, int, int]]:
    """Сгенерировать расписание групповых матчей.

    Возвращает список кортежей (буква_группы, номер_матча_1..72,
    позиция_хозяев_1..4, позиция_гостей_1..4).
    """
    result: list[tuple[str, int, int, int]] = []
    match_number = 1
    for letter in GROUPS:
        for home_pos, away_pos in GROUP_PAIRINGS:
            result.append((letter, match_number, home_pos, away_pos))
            match_number += 1
    return result
